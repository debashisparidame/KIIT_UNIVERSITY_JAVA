package KIIT_University.Assignment_1;

//3. Write a program to declare different variables of different data types used in java
//and also show that values you have assigned. (int, float, double, char, Boolean, String, long)

public class DifferentVariables {
    public static void main(String[] args) {
        int a = 10;
        float b = 12.5f;
        double c = 22.5;
        char d = 'M';
        Boolean obj = true;
        String name = "Debashis";
        long num = 23998765006L;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(obj);
        System.out.println(name);
        System.out.println(num);
    }
}
